package logika.aplikacja;

public class Auto {

    String marka;
    String model;
    String rejestracja;
    String kolor;
    String rodzajPaliwa;

    public Auto(String marka,String model,String rejestracja,String kolor,String rodzajPaliwa){
        this.marka = marka;
        this.model = model;
        this.rejestracja = rejestracja;
        this.kolor = kolor;
        this.rodzajPaliwa = rodzajPaliwa;
    }

}
